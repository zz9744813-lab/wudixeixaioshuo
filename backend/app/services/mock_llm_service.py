"""
Mock LLM Service - 模拟 LLM 服务
用于在没有真实 API 的情况下测试完整流程
"""

import asyncio
import random
from typing import AsyncGenerator, Optional


class MockLLMService:
    """
    Mock LLM 服务
    模拟真实 LLM 的行为，包括延迟、token 计数、错误等
    """

    def __init__(self):
        self.mock_responses = {
            "planner": self._mock_planner_response,
            "draft": self._mock_draft_response,
            "critic": self._mock_critic_response,
            "rewrite": self._mock_rewrite_response,
            "continuity": self._mock_continuity_response,
            "learning": self._mock_learning_response,
            "split": self._mock_split_response,
            "analyze": self._mock_analyze_response,
        }

    async def generate(
        self,
        prompt: str,
        role: str = "draft",
        temperature: float = 0.7,
        max_tokens: int = 4000,
        stream: bool = False,
    ) -> dict:
        """
        生成响应

        Args:
            prompt: 输入提示词
            role: Agent 角色
            temperature: 温度参数
            max_tokens: 最大 token 数
            stream: 是否流式输出

        Returns:
            包含响应内容的字典
        """
        # 模拟延迟 (0.5-2 秒)
        delay = random.uniform(0.5, 2.0)
        await asyncio.sleep(delay)

        # 根据角色获取响应
        response_generator = self.mock_responses.get(role, self._mock_default_response)
        content = response_generator(prompt)

        # 计算 token (简单估算: 1 token ≈ 4 字符)
        input_tokens = len(prompt) // 4
        output_tokens = len(content) // 4

        # 估算成本 (基于 OpenAI GPT-3.5 价格)
        input_cost = input_tokens * 0.0000015  # $0.0015 per 1K tokens
        output_cost = output_tokens * 0.000002  # $0.002 per 1K tokens
        total_cost = input_cost + output_cost

        return {
            "content": content,
            "model": "mock-gpt-3.5-turbo",
            "provider": "mock",
            "input_tokens": input_tokens,
            "output_tokens": output_tokens,
            "total_tokens": input_tokens + output_tokens,
            "cost": total_cost,
            "duration_seconds": delay,
        }

    async def generate_stream(
        self,
        prompt: str,
        role: str = "draft",
        temperature: float = 0.7,
        max_tokens: int = 4000,
    ) -> AsyncGenerator[str, None]:
        """流式生成响应"""
        response = await self.generate(prompt, role, temperature, max_tokens)
        content = response["content"]

        # 模拟流式输出
        words = content.split()
        for word in words:
            yield word + " "
            await asyncio.sleep(0.05)

